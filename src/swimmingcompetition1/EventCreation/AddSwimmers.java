    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       jButton1.setEnabled(true);
       jRadioButton1.setEnabled(false);
       jRadioButton2.setEnabled(false);
        String name = jComboBox1.getSelectedItem().toString();
        if(jRadioButton1.isSelected()==true){
            for(int i=0; i<maleSwimList.size();i++){
                if(name==maleSwimList.get(i).getName()){
                    swimList.add(maleSwimList.get(i));
                    maleSwimList.remove(i);
                }
            }
            SUI.setGender("Male");
        }
        
        if(jRadioButton2.isSelected()==true){
            for(int i=0; i<femaleSwimList.size();i++){
                if(name==femaleSwimList.get(i).getName()){
                    swimList.add(femaleSwimList.get(i));
                    femaleSwimList.remove(i);
                }
            }
            SUI.setGender("Female");
        }
        
        String s= String.format("%4s", Integer.toString(id)).replace(' ', '0');
        try{
            if(id==5){
                jButton2.setEnabled(false);
            }
            jTable1.setValueAt("LANE"+s, id-1, 0);
            jTable1.setValueAt(name, id-1, 1);
            id++;
        }
        catch(ArrayIndexOutOfBoundsException e){
            System.out.println(e);
        }
        jComboBox1.removeItem(name);
        if(jComboBox1.getItemCount()==0){
            jButton2.setEnabled(false);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

