    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = jTextField1.getText();
        String s= String.format("%4s", Integer.toString(id+1)).replace(' ', '0');
        try{
            jTable1.setValueAt("JU"+s, id, 0);
            jTable1.setValueAt(name, id, 1);
        }
        catch(ArrayIndexOutOfBoundsException E){
            DefaultTableModel yourModel = (DefaultTableModel) jTable1.getModel();
            yourModel.addRow(new Object[]{"JU"+s,name});
        }
        
        createJudge("JU"+s, name);
        
        id++;
        jTextField1.setText(null);
        jButton1.setEnabled(true);
    }//GEN-LAST:event_jButton2ActionPerformed

